package kmitl.it.recipe.recipe.model;

public class Mymenu {
    String menu;
    String type;
    String date;
    String writer;

    public Mymenu() { }

    public Mymenu(String menu, String type, String date, String writer) {
        this.menu = menu;
        this.type = type;
        this.date = date;
        this.writer = writer;
    }
}
